                            Pada zaman pemerintahan Hindia Belanda, kebutuhan air bersih masyarakat di kota Kendal dipenuhi melalui pembuatan sumur-sumur artetis untuk umum. Sumur-sumur ini tidak memerlukan pompa air karena air keluar dengan sendirinya, sehingga masyarakat dapat mengambil air secara gratis.
