                            Pada tahun 2008, Peraturan Daerah Kabupaten Kendal No. 8 Tahun 2008 tentang Perusahaan Daerah Air Minum "Tirto Panguripan" disahkan pada tanggal 17 Juni 2008. Selanjutnya, pada tahun 2020, melalui Peraturan Daerah Kabupaten Kendal No. 9 Tahun 2020, status PDAM diubah menjadi Perusahaan Umum Daerah Air Minum Tirto Panguripan pada tanggal 30 September 2020.

                            Terakhir, pada tahun 2022, Bupati Kendal menetapkan Lambang dan Logo Perusahaan Umum Daerah Air Minum Tirto Panguripan melalui Keputusan Bupati Kendal No. 539/205/2022 pada tanggal 27 April 2022.
